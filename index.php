			</div>
			
			<div class="boxAparelho">
				<div class="titleAparelho">ITC</div>
				<div class="imgAparelho itc"></div>
				<div class="descAparelho">Sigla em inglês para In The Canal, em tradução livre para o português “dentro do canal”. É um tipo de aparelho auditivo posicionado e adaptado dentro do conduto auditivo e ocupando uma pequena parte da concha do pavilhão da orelha externa. É necessária confecção de molde do conduto auditivo para adaptação desse tipo de aparelho.</div>
			</div>

			<div class="boxAparelho">
				<div class="titleAparelho">CIC</div>
				<div class="imgAparelho cic"></div>
				<div class="descAparelho">Sigla para Completely In the Canal, em tradução livre para o português “completamente dentro do canal”. É um tipo de aparelho auditivo posicionado e adaptado dentro do conduto auditivo. É necessária confecção de molde do conduto auditivo para adaptação da maioria dos modelos desse tipo de aparelho, mas atualmente existem opções de CIC de adaptação imediata, sem necessidade de molde.</div>
			</div>

			<div class="boxAparelho">
				<div class="titleAparelho">BTE</div>
				<div class="imgAparelho bte"></div>
				<div class="descAparelho">Sigla para Behind The Ear, em tradução livre para o português “Atrás da Orelha”. É um tipo de aparelho auditivo posicionado e adaptado atrás do pavilhão da orelha. Também pode ser chamado de Retroauricular. Modelos BTE encaminham o som para o ouvido através de um tubo transparente e molde do ouvido, feito sob medida, ou com adaptação aberta. A adaptação aberta pode ser realizada de forma imediata com peças de silicone, de diferentes formatos e tamanhos, que se encaixam no conduto auditivo. </div>
			</div>

			<div class="boxAparelho">
				<div class="titleAparelho">RIC</div>
				<div class="imgAparelho ric"></div>
				<div class="descAparelho">Siglas em inglês para Receiver In The Ear (RITE), Receiver In Canal (RIC) e Receiver In Ear (RIE), que significa receptor dentro do canal auditivo ou da orelha. São nomes diferentes para o mesmo tipo de aparelho auditivo posicionado atrás da orelha, como o BTE, mas com o seu receptor adaptado dentro do conduto auditivo, com peças de silicone ou confecção de pequeno molde sob medida.</div>
